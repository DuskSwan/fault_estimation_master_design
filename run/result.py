def make_df(scores):
    df = pd.DataFrame([], columns=features)
    for score in scores:
        df = df._append(score, ignore_index=True)
    df.index = [str(i+1) for i in range(len(scores))]
    dfsum = df.sum(axis=0).rename('sum')
    df = df._append(dfsum)
    return df

